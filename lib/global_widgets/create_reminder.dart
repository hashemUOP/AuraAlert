  // Method to store user ID from Firebase Auth and store it in a class-scoped variable
  Future<void> getUserID() async {
    User? user = FirebaseAuth.instance.currentUser;
    if (user != null) {
      setState(() {
        userId = user.uid;
        userEmail = user.email ?? "";
        userPhone = user.phoneNumber ?? "";
      });
    }
